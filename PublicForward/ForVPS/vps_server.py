@app.post("/v1/chat/completions")
async def chat_completions(request: Request, req: ChatCompletionRequest):
    """Enhanced chat completions with all new features."""
    start_time = time.time()

    try:
        # Feature 8: Resolve think_mode alias
        think_mode_params = _resolve_think_mode(req)
        model_tab = req.model_tab or think_mode_params.get("model_tab")
        deep_think = (
            req.deep_think
            if req.deep_think is not None
            else think_mode_params.get("deep_think", False)
        )
        web_search = (
            req.web_search
            if req.web_search is not None
            else think_mode_params.get("web_search", False)
        )

        # Apply model alias if no explicit model_tab
        if not model_tab:
            alias = MODEL_ALIASES.get(req.model)
            if alias:
                model_tab = alias["model_tab"]
                if req.deep_think is None:
                    deep_think = alias["deep_think"]

        # Default to instant if still None
        model_tab = model_tab or "instant"

        # Generate or use provided session_id
        session_id = req.session_id or f"sess-{uuid.uuid4().hex[:16]}"
        mode = req.mode or "new"

        # Feature 9: Convert attachments to worker format
        attachments = None
        if req.attachments:
            attachments = [
                {
                    "filename": att.filename,
                    "data": att.data,
                    "mime_type": att.mime_type,
                }
                for att in req.attachments
            ]

        # Dispatch to worker
        result = await worker_mgr.dispatch(
            prompt=req.last_user_message(),
            mode=mode,
            session_id=session_id,
            model_tab=model_tab,
            deep_think=deep_think,
            web_search=web_search,
            attachments=attachments,
            preferred_account=req.preferred_account,
        )

        if not result.get("ok"):
            raise HTTPException(status_code=500, detail=result.get("error", "Unknown error"))

        # Build OpenAI-compatible response
        response_text = result.get("text", "")
        prompt_tokens = _token_estimate(req.last_user_message())
        completion_tokens = _token_estimate(response_text)

        # Feature 10: finish_reason and tool_calls support
        finish_reason = "stop"
        tool_calls = None
        if result.get("tool_calls"):
            finish_reason = "tool_calls"
            tool_calls = result["tool_calls"]

        choice = {
            "index": 0,
            "message": {
                "role": "assistant",
                "content": response_text,
            },
            "finish_reason": finish_reason,
        }

        if tool_calls:
            choice["message"]["tool_calls"] = tool_calls

        # Feature 11: x_meta block with extended metadata
        x_meta = {
            "session_id": session_id,
            "account": result.get("account"),
            "conversation_url": result.get("conversation_url"),
            "model_tab": model_tab,
            "deep_think": deep_think,
            "web_search": web_search,
            "response_time": time.time() - start_time,
            "timestamp": time.time(),
        }

        response_data = {
            "id": _make_id(),
            "object": "chat.completion",
            "created": int(time.time()),
            "model": req.model,
            "choices": [choice],
            "usage": {
                "prompt_tokens": prompt_tokens,
                "completion_tokens": completion_tokens,
                "total_tokens": prompt_tokens + completion_tokens,
            },
            "x_meta": x_meta,  # Feature 11
        }

        # Feature 6, 7: Add custom headers
        headers = {
            "X-Session-ID": session_id,
        }
        if result.get("account"):
            headers["X-Account-Name"] = result["account"]
        if result.get("conversation_url"):
            headers["X-Conversation-URL"] = result["conversation_url"]

        return JSONResponse(content=response_data, headers=headers)

    except HTTPException:
        raise
    except TimeoutError as e:
        raise HTTPException(status_code=504, detail=str(e))
    except Exception as e:
        raise HTTPException(status_code=500, detail=str(e))


