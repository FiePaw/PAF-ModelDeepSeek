@app.websocket("/ws/worker")
async def worker_endpoint(ws: WebSocket):
    await ws.accept()
    worker_id: Optional[str] = None
    try:
        # Registration
        msg = await ws.receive_json()
        if msg.get("type") != "register":
            await ws.close(code=4000, reason="Expected register message")
            return

        token = msg.get("token")
        if token != AUTH_TOKEN:
            await ws.close(code=4001, reason="Invalid token")
            return

        hostname = msg.get("hostname", "unknown")
        max_concurrent = msg.get("max_concurrent", 1)
        accounts = msg.get("accounts", [])

        worker_id = await worker_mgr.register(ws, hostname, max_concurrent, accounts)
        await ws.send_json({"type": "registered", "worker_id": worker_id})

        # Message loop
        async for raw in ws.iter_json():
            mtype = raw.get("type")
            if mtype == "result":
                await worker_mgr.handle_result(raw)
            elif mtype == "error":
                await worker_mgr.handle_error(raw)
            elif mtype == "update_accounts":
                await worker_mgr.update_accounts(worker_id, raw.get("accounts", []))
            elif mtype == "ping":
                # Update last_ping timestamp
                if worker_id in worker_mgr.workers:
                    worker_mgr.workers[worker_id].last_ping = time.time()
                await ws.send_json({"type": "pong"})
            elif mtype == "pong":
                if worker_id in worker_mgr.workers:
                    worker_mgr.workers[worker_id].last_ping = time.time()

    except WebSocketDisconnect:
        pass
    except Exception as e:
        print(f"[vps] worker error: {e}")
    finally:
        if worker_id:
            await worker_mgr.unregister(worker_id)


