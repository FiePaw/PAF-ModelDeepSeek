# =========================================================================== #
# Pydantic models — OpenAI Chat Completions schema
# =========================================================================== #
class ChatMessage(BaseModel):
    role: str
    content: Any  # str, or list of content parts (for multimodal)
    name: Optional[str] = None


class ToolFunctionDef(BaseModel):
    name: str
    description: Optional[str] = None
    parameters: Optional[dict] = None


class Tool(BaseModel):
    type: str = "function"
    function: ToolFunctionDef


class AttachmentPayload(BaseModel):
    """Feature 9: Enhanced attachment with filename + data + mime_type."""

    filename: str
    data: str  # base64-encoded
    mime_type: str


class ChatCompletionRequest(BaseModel):
    model: str = "deepseek-chat"
    messages: list[ChatMessage]
    stream: bool = False
    temperature: Optional[float] = None
    max_tokens: Optional[int] = None
    tools: Optional[list[Tool]] = None
    # Custom DeepSeek extensions:
    deep_think: Optional[bool] = None
    web_search: Optional[bool] = None
    model_tab: Optional[str] = None
    session_id: Optional[str] = None
    mode: Optional[str] = None  # "new" | "continue"
    attachments: Optional[list[AttachmentPayload]] = None
    # Feature 8: think_mode alias
    think_mode: Optional[str] = None
    # Feature 4: preferred_account routing
    preferred_account: Optional[str] = None

    def last_user_message(self) -> str:
        for msg in reversed(self.messages):
            if msg.role == "user":
                if isinstance(msg.content, str):
                    return msg.content
                if isinstance(msg.content, list):
                    parts = [
                        p.get("text", "")
                        for p in msg.content
                        if isinstance(p, dict) and p.get("type") == "text"
                    ]
                    return "\n".join(parts)
        return ""


# Rebuild models
for _m in (ChatMessage, ToolFunctionDef, Tool, AttachmentPayload, ChatCompletionRequest):
    _m.model_rebuild()


