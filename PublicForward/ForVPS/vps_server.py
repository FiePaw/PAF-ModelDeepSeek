"""
PublicForward/ForVPS/vps_server.py — Enhanced VPS Bridge (FastAPI + WebSocket).

Exposes an OpenAI-compatible REST API (/v1/chat/completions, /v1/models) and
bridges requests over WebSocket to one or more local workers (public.py),
each of which drives a pool of real DeepSeek browsers.

NEW FEATURES:
  1. CORS middleware
  2. Worker ID generated by VPS (not from worker)
  3. Worker wait timeout (queue requests with timeout)
  4. update_accounts from worker (runtime account updates)
  5. /v1/models show live accounts from workers
  6. X-Session-ID response header
  7. X-Account-Name + X-Conversation-URL headers
  8. think_mode field (DeepSeek alias)
  9. Attachment with "filename"+"data"+"mime_type"
  10. finish_reason + tool_calls response path (OpenAI-compatible)
  11. x_meta block in response (session, account, timing, conversation_url)
  12. Worker stats detail in /health and /
  13. reject_future() when worker sends error message

Run:
  python vps_server.py --port 8000 --token MY_SHARED_SECRET
"""
