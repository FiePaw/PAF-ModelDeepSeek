def _token_estimate(text: str) -> int:
    if not text:
        return 0
    return max(1, len(text) // 4)


def _make_id() -> str:
    return f"chatcmpl-{uuid.uuid4().hex[:24]}"


def _resolve_think_mode(req: ChatCompletionRequest) -> dict:
    """Feature 8: Resolve think_mode to DeepSeek parameters."""
    if req.think_mode:
        alias = THINK_MODE_ALIASES.get(req.think_mode.lower())
        if alias:
            return alias
    return {}


# =========================================================================== #
# WorkerManager
# =========================================================================== #
class WorkerEntry:
    def __init__(
        self, worker_id: str, ws: WebSocket, max_concurrent: int, accounts: list[str]
    ) -> None:
        self.worker_id = worker_id
        self.ws = ws
        self.max_concurrent = max_concurrent
        self.accounts = accounts
        self.in_flight = 0
        self.connected_at = time.time()
        self.last_ping = time.time()


class WorkerManager:
    def __init__(self) -> None:
        self.workers: dict[str, WorkerEntry] = {}
        self._futures: dict[str, asyncio.Future] = {}
        self._session_worker: dict[str, str] = {}
        self._rr_index = 0
        self._lock = asyncio.Lock()
        self._worker_counter = 0

    # ----- Feature 2: VPS-generated worker IDs ------------------------ #
    def _generate_worker_id(self, hostname: str) -> str:
        """Generate unique worker ID on VPS side."""
        self._worker_counter += 1
        return f"worker-{hostname}-{self._worker_counter:03d}"

    # ----- registration ---------------------------------------------- #
    async def register(
        self, ws: WebSocket, hostname: str, max_concurrent: int, accounts: list[str]
    ) -> str:
        worker_id = self._generate_worker_id(hostname)
        async with self._lock:
            self.workers[worker_id] = WorkerEntry(worker_id, ws, max_concurrent, accounts)
        print(f"[vps] worker registered: {worker_id} accounts={accounts}")
        return worker_id

    async def unregister(self, worker_id: str) -> None:
        async with self._lock:
            self.workers.pop(worker_id, None)
            # Clean up session affinity
            to_remove = [k for k, v in self._session_worker.items() if v == worker_id]
            for k in to_remove:
                self._session_worker.pop(k, None)
        print(f"[vps] worker unregistered: {worker_id}")

    # ----- Feature 4: update_accounts runtime ------------------------ #
    async def update_accounts(self, worker_id: str, accounts: list[str]) -> None:
        """Update account list for a worker at runtime."""
        async with self._lock:
            if worker_id in self.workers:
                self.workers[worker_id].accounts = accounts
                print(f"[vps] updated accounts for {worker_id}: {accounts}")

    # ----- Feature 3: Worker wait timeout ---------------------------- #
    async def dispatch(
        self,
        prompt: str,
        mode: str = "new",
        session_id: Optional[str] = None,
        preferred_account: Optional[str] = None,
        **kwargs,
    ) -> dict:
        """Dispatch task with worker wait timeout."""
        # Session affinity for continue mode
        if mode == "continue" and session_id and session_id in self._session_worker:
            worker_id = self._session_worker[session_id]
            if worker_id in self.workers:
                return await self._dispatch_to_worker(
                    worker_id, prompt, mode, session_id, preferred_account, **kwargs
                )

        # Feature 3: Wait for available worker with timeout
        start_wait = time.time()
        while time.time() - start_wait < WORKER_WAIT_TIMEOUT:
            worker_id = await self._pick_worker(preferred_account)
            if worker_id:
                # Store session affinity
                if session_id:
                    self._session_worker[session_id] = worker_id
                return await self._dispatch_to_worker(
                    worker_id, prompt, mode, session_id, preferred_account, **kwargs
                )
            await asyncio.sleep(0.5)

        raise TimeoutError("No available worker within timeout")

    async def _pick_worker(self, preferred_account: Optional[str] = None) -> Optional[str]:
        """Pick least-loaded worker, optionally filtering by account."""
        async with self._lock:
            candidates = [
                w
                for w in self.workers.values()
                if w.in_flight < w.max_concurrent
                and (not preferred_account or preferred_account in w.accounts)
            ]
            if not candidates:
                return None
            # Round-robin among candidates
            self._rr_index = (self._rr_index + 1) % len(candidates)
            return candidates[self._rr_index].worker_id

    async def _dispatch_to_worker(
        self,
        worker_id: str,
        prompt: str,
        mode: str,
        session_id: Optional[str],
        preferred_account: Optional[str],
        **kwargs,
    ) -> dict:
        worker = self.workers.get(worker_id)
        if not worker:
            raise RuntimeError(f"Worker {worker_id} not found")

        task_id = f"task-{uuid.uuid4().hex[:12]}"
        future: asyncio.Future = asyncio.Future()
        self._futures[task_id] = future

        worker.in_flight += 1

        try:
            await worker.ws.send_json(
                {
                    "type": "task",
                    "task_id": task_id,
                    "request": {
                        "prompt": prompt,
                        "mode": mode,
                        "session_id": session_id,
                        "preferred_account": preferred_account,
                        **kwargs,
                    },
                }
            )
            result = await asyncio.wait_for(future, timeout=REQUEST_TIMEOUT)
            return result
        finally:
            worker.in_flight -= 1
            self._futures.pop(task_id, None)

    # ----- result handling ------------------------------------------- #
    async def handle_result(self, msg: dict) -> None:
        task_id = msg.get("task_id")
        if task_id in self._futures:
            self._futures[task_id].set_result(msg.get("result", {}))

    # ----- Feature 13: reject_future on error ------------------------ #
    async def handle_error(self, msg: dict) -> None:
        """Immediately reject future when worker sends error."""
        task_id = msg.get("task_id")
        if task_id in self._futures:
            error_msg = msg.get("error", "Worker error")
            status = msg.get("status", 500)
            # Create HTTPException to propagate status
            exc = HTTPException(status_code=status, detail=error_msg)
            self._futures[task_id].set_exception(exc)

    # ----- Feature 12: Worker stats ---------------------------------- #
    def get_stats(self) -> dict:
        """Get detailed worker statistics."""
        return {
            "total_workers": len(self.workers),
            "workers": [
                {
                    "worker_id": w.worker_id,
                    "accounts": w.accounts,
                    "max_concurrent": w.max_concurrent,
                    "in_flight": w.in_flight,
                    "connected_at": w.connected_at,
                    "uptime_seconds": time.time() - w.connected_at,
                }
                for w in self.workers.values()
            ],
            "total_accounts": sum(len(w.accounts) for w in self.workers.values()),
            "busy_slots": sum(w.in_flight for w in self.workers.values()),
        }

    # ----- Feature 5: List all available accounts -------------------- #
    def list_all_accounts(self) -> list[str]:
        """List all unique accounts across all workers."""
        accounts = set()
        for w in self.workers.values():
            accounts.update(w.accounts)
        return sorted(accounts)


